import { createHash } from "node:crypto";
import { readFile, writeFile } from "node:fs/promises";
import { format } from "prettier";

const source = new globalThis.URL("../../domain-spec/catalog.json", import.meta.url);
const destination = new globalThis.URL(
  "../packages/domain-catalog/src/catalog.generated.ts",
  import.meta.url,
);
const conformanceSource = new globalThis.URL(
  "../../domain-spec/conformance/v1.json",
  import.meta.url,
);
const conformanceDestination = new globalThis.URL(
  "../packages/domain-catalog/src/conformance.generated.ts",
  import.meta.url,
);
const [sourceText, conformanceSourceText] = await Promise.all([
  readFile(source, "utf8"),
  readFile(conformanceSource, "utf8"),
]);
const catalog = JSON.parse(sourceText);
const conformance = JSON.parse(conformanceSourceText);

if (catalog.schemaVersion !== 1 || typeof catalog.catalogVersion !== "string") {
  throw new Error("Unsupported or malformed canonical domain catalog.");
}
if (
  conformance.schemaVersion !== 1 ||
  conformance.catalogVersion !== catalog.catalogVersion ||
  !Array.isArray(conformance.vectors)
) {
  throw new Error("Unsupported, malformed, or catalog-mismatched conformance vectors.");
}

const fingerprint = createHash("sha256").update(sourceText).digest("hex");
const conformanceFingerprint = createHash("sha256").update(conformanceSourceText).digest("hex");
const output = await format(
  `// Generated by scripts/generate-domain-catalog.mjs. Do not edit.\n` +
    `export const canonicalCatalog = ${JSON.stringify(catalog, null, 2)} as const;\n\n` +
    `export const canonicalCatalogSha256 = ${JSON.stringify(fingerprint)} as const;\n`,
  { parser: "typescript" },
);
const conformanceOutput = await format(
  `// Generated by scripts/generate-domain-catalog.mjs. Do not edit.\n` +
    `export const canonicalConformance = ${JSON.stringify(conformance, null, 2)} as const;\n\n` +
    `export const canonicalConformanceSha256 = ${JSON.stringify(conformanceFingerprint)} as const;\n`,
  { parser: "typescript" },
);

await Promise.all([
  writeFile(destination, output, "utf8"),
  writeFile(conformanceDestination, conformanceOutput, "utf8"),
]);
globalThis.console.log(
  `Generated domain catalog ${catalog.catalogVersion} (${fingerprint.slice(0, 12)}) ` +
    `and ${conformance.vectors.length} vectors (${conformanceFingerprint.slice(0, 12)}).`,
);
